// Tipos de dados trabalhando com os booleans

// Conversão implicita
const numero = 456;
const numeroString = Number('456');

console.log(numero === numeroString);

// dois iguais (==) faz uma comparação pelo valor
// Três iguais (===) compara não só o valor, mas também o tipo, se é String, number etc;

console.log(numero + numeroString);

// Conversão Explicita

// Number()
// String()

console.log(numero + numeroString);
